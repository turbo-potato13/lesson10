package com.vtb.kortunov.lesson10.stages;

import com.vtb.kortunov.lesson10.Car;
import com.vtb.kortunov.lesson10.Race;

import java.util.concurrent.Semaphore;

public class Tunnel extends Stage {
    private final Semaphore smp = new Semaphore(Race.COMPETITORS_COUNT / 2);

    public Tunnel() {
        this.length = 80;
        this.description = "Тоннель " + length + " метров";
    }

    @Override
    public void overcome(Car c) {
        try {
            try {
                System.out.println(c.getName() + " готовится к этапу(ждет): " + description);
                smp.acquire();
                System.out.println(c.getName() + " начал этап: " + description);
                Thread.sleep(length / c.getSpeed() * 1000);
            } catch (InterruptedException e) {
                e.printStackTrace();
            } finally {
                System.out.println(c.getName() + " закончил этап: " + description);
                smp.release();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
}

